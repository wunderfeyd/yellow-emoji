	// Transform HTML
	function onParseContentText($page, $text)
	{
		return $this->emojifyHTML($text);
	}
	
	// Check if element does not allow emoji insertion
	function isFixedElement($elementName)
	{
		return preg_match("/^(code|textarea)/i", $elementName)?1:0;
	}
	
	// Grab text from HTML and do emojifyText on it (code from createTextDescription)
	function emojifyHTML($text)
	{
		$elementsOpen = array();
		$elementsDepth = 0;
		while(true)
		{
			$elementFound = preg_match("/<\s*?([\/!]?\w*)(.*?)\s*?\>/s", $text, $matches, PREG_OFFSET_CAPTURE, $offsetBytes);
			$element = $matches[0][0];
			$elementName = $matches[1][0];
			$elementText = $matches[2][0];
			$elementOffsetBytes = $elementFound ? $matches[0][1] : strlenb($text);
			$string = substrb($text, $offsetBytes, $elementOffsetBytes - $offsetBytes);
			if($elementsDepth==0) $string = $this->emojifyText($string);
			$output .= $string;
			if(!$elementFound) break;
			if(!empty($elementName) && substru($elementText, -1)!='/' &&
			   !preg_match("/^(area|br|col|hr|img|input|col|param|!)/i", $elementName))
			{
				if($elementName[0]!='/')
				{
					$elementsDepth += $this->isFixedElement($elementName);
					array_push($elementsOpen, $elementName);
				} else {
					$elementsDepth -= $this->isFixedElement($elementsOpen[count($elementsOpen)-1]);
					array_pop($elementsOpen);
				}
			}
			$output .= $element;
			$offsetBytes = $elementOffsetBytes+strlenb($element);
		}
		return $output;
	}

	// Insert emojis in text
	function emojifyText($text)
	{
		$this->loadTranslations();
		$text = preg_replace_callback(array_keys($this->shortToUtf8), array(&$this, 'transformShortToImage'), $text);
		$text = preg_replace_callback(array_keys($this->utf8ToShort), array(&$this, 'transformUtf8ToImage'), $text);
		return $text;
	}
	
	function buildImageString($name, $image)
	{
		return "<img class=\"emoji\" title=\"".$name."\" alt=\"".$name."\" src=\"".$this->yellow->config->get("emojisImageLocation").$image."\"/>";
	}

	function transformShortToImage($matches)
	{
		return $this->buildImageString($matches[0], $this->utf8ToImage[$this->shortToUtf8['/'.preg_quote($matches[0]).'/']]);
	}
	
	function transformUtf8ToImage($matches)
	{
		return $this->buildImageString($this->utf8ToShort['/'.preg_quote($matches[0]).'/'], $this->utf8ToImage[$matches[0]]);
	}
	
	// Transform short names to utf8
	function onEmojiTransformShortToUtf8($text)
	{
		$this->loadTranslations();
		return preg_replace_callback(array_keys($this->shortToUtf8), array(&$this, 'transformShortToUtf8'), $text);
	}

	function transformShortToUtf8($matches)
	{
		return $this->shortToUtf8['/'.preg_quote($matches[0]).'/'];
	}

	// Transform utf8 to short names
	function onEmojiTransformUtf8ToShort($text)
	{
		$this->loadTranslations();
		return preg_replace_callback(array_keys($this->utf8ToShort), array(&$this, 'transformUtf8ToShort'), $text);
	}

	function transformUtf8ToShort($matches)
	{
		return $this->utf8ToImage[$this->shortToUtf8['/'.preg_quote($matches[0]).'/']];
	}

	// Load translations
	function loadTranslations($text)
	{	
		if(!$this->cached)
		{
			$this->cached = true;
			foreach($this->yellow->plugins->plugins as $key=>$value)
			{
				if(method_exists($value["obj"], "onEmojiTranslations"))
				{
					$output = $value["obj"]->onEmojiTranslations($this->yellow->config->get("emojisSet"));
					if(!is_null($output))
					{
						foreach($output as $emoji)
						{
							$this->utf8ToImage[$emoji['utf8']] = $emoji['image'];
							$this->utf8ToShort['/'.preg_quote($emoji['utf8']).'/'] = $emoji['short'];
							$this->shortToUtf8['/'.preg_quote($emoji['short']).'/'] = $emoji['utf8'];
						}
					}
				}
			}
		}
	}
}
		
